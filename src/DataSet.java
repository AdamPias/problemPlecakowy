public class DataSet {
    private int[] sizes;
    private int[] vals;

    public int[] getSizes() {
        return sizes;
    }

    public void setSizes(int[] sizes) {
        this.sizes = sizes;
    }

    public int[] getVals() {
        return vals;
    }

    public void setVals(int[] vals) {
        this.vals = vals;
    }
}
